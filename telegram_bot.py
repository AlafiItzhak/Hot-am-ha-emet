def send_post(text):
    """שולח פוסט לערוץ"""
    data = {
        "chat_id": CHAT_ID,
        "text": text,
        "parse_mode": "Markdown",
        "disable_web_page_preview": False
    }
    response = requests.post(URL, json=data)
    return response.json()


def get_post_index():
    """קורא את מספר הפוסט הנוכחי מקובץ"""
    try:
        with open("post_index.txt", "r") as f:
            return int(f.read().strip())
    except:
        return 0


def save_post_index(index):
    """שומר את מספר הפוסט הנוכחי"""
    with open("post_index.txt", "w") as f:
        f.write(str(index))


def send_daily_post():
    """שולח את הפוסט היומי"""
    index = get_post_index()
    
    if index >= len(POSTS):
        print("סיימנו את כל 30 הפוסטים! מתחיל מחדש...")
        index = 0
    
    post = POSTS[index]
    result = send_post(post)
    
    if result.get("ok"):
        print(f"✓ פוסט {index + 1} נשלח בהצלחה! ({datetime.now().strftime('%d/%m/%Y %H:%M')})")
        save_post_index(index + 1)
    else:
        print(f"✗ שגיאה בשליחת פוסט: {result}")
    
    return result


