def send_post(text):
    data = {
        "chat_id": CHAT_ID,
        "text": text,
        "parse_mode": "Markdown",
        "disable_web_page_preview": False
    }
    response = requests.post(URL, json=data)
    return response.json()


def get_post_index():
    """מחשב את מספר הפוסט לפי מספר הימים מתאריך ההתחלה"""
    today = date.today()
    days_passed = (today - START_DATE).days
    index = days_passed % len(POSTS)
    return index


def send_daily_post():
    index = get_post_index()
    post = POSTS[index]
    result = send_post(post)
    if result.get("ok"):
        print(f"✓ פוסט {index + 1} נשלח! ({datetime.now().strftime('%d/%m/%Y %H:%M')})")
    else:
        print(f"✗ שגיאה: {result}")
    return result


